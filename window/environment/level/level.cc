void Level::render_level(){
  size_t h, w;
  for (size_t i = 0; i != m_level_desc.size(); ++i)
    if (m_level_desc[i] & 1){
      w = i % m_level_width;
      h = i / m_level_width + 1;
      m_level_sprite->blit(Essential::to_screen_x( 32 * w), Essential::to_screen_y(32 * h));
    }

}

bool Level::does_hit(int x, int y){
  y /= 32;
  x /= 32;
  // if (y < 0 || x + m_level_width * y > m_level_height * m_level_width)
  //   return true;

  return m_level_desc[x + m_level_width * y] & 1;
}
